def generate_loot(location_type):
    loot = []
    location_data = loot_data[location_type]
    num_items = random.randint(location_data["min_items"], location_data["max_items"])

    for _ in range(num_items):
        loot_type = random.choices(
            list(location_data["loot_chances"].keys()),
            weights=[chance[0] for chance in location_data["loot_chances"].values()],
            k=1
        )[0]

        item_data = location_data["loot_chances"][loot_type][1]
        item_name = random.choice(item_data)[0]

        # Получаем диапазон для количества предметов
        quantity_range = next(item for item in item_data if item[0] == item_name)[1]
        item_quantity = random.randint(quantity_range[0], quantity_range[1])

        loot.append((item_name, item_quantity))

    return loot

def main():
    location_type = input("Введите тип локации ('простая', 'сложная', 'тяжелая'): ").strip().lower()
    
    if location_type not in loot_data:
        print("Некорректный тип локации. Пожалуйста выберите 'простая', 'сложная' или 'тяжелая'.")
        return

    loot = generate_loot(location_type)
    
    print(f"\nСгенерированный лут для {location_type} локации:")
    for item_name, quantity in loot:
        print(f"{item_name}: {quantity}")
    input()

if __name__ == "__main__":
    main()