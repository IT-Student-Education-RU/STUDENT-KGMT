#Динамическая типизация
# Первый тип данных: целое число
variable = 10
print(type(variable))  # Вывод: <class 'int'>

# Изменение значения на строку
variable = "Hello"
print(type(variable))  # Вывод: <class 'str'>

# Изменение значения на число с плавающей запятой
variable = 3.14
print(type(variable))  # Вывод: <class 'float'>

