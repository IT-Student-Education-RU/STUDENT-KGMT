# Запрашиваем у пользователя способ оплаты
payment_method = input("Какой способ оплаты вы предпочитаете? (карта/наличные): ").strip().lower()

# Проверяем способ оплаты и выводим соответствующее сообщение
if payment_method == 'карта':
    print("Вы выбрали оплату картой. Пожалуйста, введите данные вашей карты.")
elif payment_method == 'наличные':
    print("Вы выбрали оплату наличными. Пожалуйста, подготовьте необходимую сумму.")
else:
    print("Некорректный ввод. Пожалуйста, введите 'карта' или 'наличные'.")

input()