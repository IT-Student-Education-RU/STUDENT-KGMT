my_teacher = "Edward Cullen"

# Обращаемся к символу по индексу 0
# В Python индексация начинается с 0, поэтому my_teacher[0] вернет первую букву
print(my_teacher[0])  # 'E'

# Обращаемся к символу по индексу 2
print(my_teacher[2])  # 'a'

# Обращаемся к символу по индексу 7
print(my_teacher[7])  # 'C'